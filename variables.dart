void main() {
  // Define and initialize variables
  int age = 42;                      // Integer variable
  double height = 5.2;              // Double variable
  String name = 'Jane';    // String variable
  bool isSingle = true;                   // Boolean variable
  List<String> companies = ['Jamii', 'Telcom', 'GreenLtd', 'RedLtd', 'TheStar'];   // List of strings

